#!/usr/bin/env bash
set -euo pipefail

INPUT="${1:-}"
TOKEN_FILE="${RUNWALL_HOME:-${SECURE_CLAUDE_CODE_HOME:-$HOME/.runwall}}/config/live-token-patterns.regex"
. "$(dirname "${BASH_SOURCE[0]}")/lib/audit.sh"
. "$(dirname "${BASH_SOURCE[0]}")/lib/patterns.sh"
trap 'shield_cleanup_pattern_files' EXIT

[ -f "$TOKEN_FILE" ] || exit 0

if ! printf '%s\n' "$INPUT" | grep -Eqi '(\.log([[:space:]]|$)|audit\.jsonl|events\.jsonl|drifts\.json|runwall-audit|\.sarif([[:space:]]|$)|summary\.md|incident-[^[:space:]]+\.(json|zip))'; then
  exit 0
fi

if ! shield_match_pattern_file "$INPUT" "$TOKEN_FILE" \
  && ! printf '%s\n' "$INPUT" | grep -Eqi '(RUNWALL_JSON:|"module":"runwall-|"event_id":"[a-f0-9]{32}"|"decision":"(allow|warn|prompt|block|redact)")'; then
  exit 0
fi

shield_audit "log-poisoning-guard" "block" "log or audit artifact poisoning detected" "$INPUT"
printf '%s\n' '[runwall] blocked log or audit artifact poisoning' >&2
printf '%s\n' 'reason: the write targets a log, report, or Runwall evidence file and includes live secrets or forged security telemetry markers' >&2
printf '%s\n' 'next: keep evidence clean, masked, and generated by the tooling instead of writing raw secrets or fake audit lines into reports' >&2
exit 2
